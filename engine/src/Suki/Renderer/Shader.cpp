#include "Suki/Renderer/Shader.h"

#include "Platform/OpenGL/OpenGLShader.h"

namespace Suki
{

std::shared_ptr<Shader> Shader::Create(const std::string& filepath)
{
    return std::make_shared<OpenGLShader>(filepath);
}

std::shared_ptr<Shader> Shader::Create(const std::string& name,
                                       const std::string& vertexSource,
                                       const std::string& fragSource)
{
    return std::make_shared<OpenGLShader>(name, vertexSource, fragSource);
}

void ShaderLibrary::Add(const std::string& name,
                        const std::shared_ptr<Shader>& shader)
{
    m_Shaders[name] = shader;
}

void ShaderLibrary::Add(const std::shared_ptr<Shader>& shader)
{
    std::string name = shader->GetName();
    Add(name, shader);
}

std::shared_ptr<Shader> ShaderLibrary::Load(const std::string& filepath)
{
    std::shared_ptr<Shader> shader = Shader::Create(filepath);
    Add(shader);

    return shader;
}

std::shared_ptr<Shader> ShaderLibrary::Load(const std::string& name,
                                            const std::string& filepath)
{
    std::shared_ptr<Shader> shader = Shader::Create(filepath);
    Add(name, shader);

    return shader;
}

std::shared_ptr<Shader> ShaderLibrary::Get(const std::string& name)
{
    return m_Shaders[name];
}

bool ShaderLibrary::Exists(const std::string& name) const
{
    return m_Shaders.find(name) != m_Shaders.end();
}

}  // namespace Suki
